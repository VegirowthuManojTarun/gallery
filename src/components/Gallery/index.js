class Gallery extends Component {
  state = {
    thumbnail: imagesList[0],
  }

  updateThumbnail = id => {
    const updatedThumbnail = imagesList.filter(img => img.id === id)
    this.setState({thumbnail: updatedThumbnail[0]})
  }

  render() {
    const {thumbnail} = this.state
    const {imageUrl, imageAltText} = thumbnail
    console.log(thumbnail)
    return (
      <div className="container">
        <img src={imageUrl} alt={imageAltText} className="thumbnail" />
        <h1>Nature Photography</h1>
        <p>Nature Photography by Tarun</p>
        <ul className="buttons">
          {imagesList.map(img => (
            <ThumbnailItem
              images={img}
              updateThumbnail={this.updateThumbnail}
              key={img.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
